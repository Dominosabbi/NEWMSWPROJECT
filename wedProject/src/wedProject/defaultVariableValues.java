package wedProject;

import java.util.Arrays;

public class defaultVariableValues {
static byte bt;
static char c;
static short sh ;
static int x;
static long l;
static float f;
static double d;
static String s;
static boolean b;

	public static void main(String[] args) {
		
		
		System.out.println(bt);
		System.out.println(c);
		System.out.println(sh);
		System.out.println(x);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(s);
		System.out.println(b);
		
		System.out.println(Byte.MAX_VALUE);
		System.out.println((int)(Math.pow(2, 7)-1));
		System.out.println(Integer.MIN_VALUE);
		System.out.println(Math.pow(2, 32-1)-1);
		System.out.println(Integer.MAX_VALUE);
		System.out.println(Integer.min(100, 50));
		System.out.println(Math.ceil(4.3));
		System.out.println(Math.floor(4.3));
		System.out.println(Math.PI);
		System.out.println(Math.pow(2, 7)-1);
		
		
	}

}
